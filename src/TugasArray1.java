    private void hitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungActionPerformed
         Double hsl = 0.0;
         Double a1,a2,a3,a4;
         a1 = Double.parseDouble(bil1.getText());
         a2 = Double.parseDouble(bil2.getText());
         
         
         if(jns.equals("penjumlahan")){
             if(jml == 2) {
               hsl = a1+a2;
                 hasil.setText(""+ a1 + " + "+ a2 + " = "+ hsl);
             }
             if(jml == 3) {
                 a3 = Double.parseDouble(bil3.getText());
                 hsl = a1+a2+a3;
                 hasil.setText(" "+ a1 + " + "+ a2 + " + " + a3 + " = "+ hsl );
             }
             if(jml == 4) {
                 a4 = Double.parseDouble(bil4.getText());
                 a3 = Double.parseDouble(bil3.getText());
                 hsl = a1+a2+a3+a4;
                 hasil.setText(" "+ a1 + " + "+ a2 + " + " + a3 + " + " + a4 + " = " + hsl );
             }
         }
         
         if(jns.equals("pengurangan")){
             if(jml == 2) {
                 hsl = a1-a2;
                 hasil.setText(" "+ a1 + " - "+ a2 +" = "+ hsl );
             }
             if(jml == 3) {
                 a3 = Double.parseDouble(bil3.getText());
                 hsl = a1-a2-a3;
                 hasil.setText(" "+ a1 + " - "+ a2 + " - " + a3 + " = "+ hsl );
             }
             if(jml == 4) {
                 a3 = Double.parseDouble(bil3.getText());
                 a4 = Double.parseDouble(bil4.getText());
                 hsl = a1-a2-a3-a4;
                 hasil.setText(" "+ a1 + " - "+ a2 + " - " + a3 + " - " + a4 + " = " + hsl );
             }
         }
         if(jns.equals("perkalian")){
             if(jml == 2) {
                 hsl = a1*a2;
                 hasil.setText(" "+ a1 + " x "+ a2 +" = "+ hsl );
             }
             if(jml == 3) {
                 a3 = Double.parseDouble(bil3.getText());
                 hsl = a1*a2*a3;
                 hasil.setText(" "+ a1 + " x "+ a2 + " x " + a3 + " = "+ hsl );
             }
             if(jml == 4) {
                 a3 = Double.parseDouble(bil3.getText());
                 a4 = Double.parseDouble(bil4.getText());
                 hsl = a1*a2*a3*a4;
                 hasil.setText(" "+ a1 + " x "+ a2 + " x " + a3 + " x " + a4 + " = " + hsl );
             }
         }
         else if(jns.equals("pembagian")){
             if(jml == 2) {
                 hsl = a1/a2;
                 hasil.setText(" "+ a1 + " : "+ a2 +" = "+ hsl );
             }
             if(jml == 3) {
                 a3 = Double.parseDouble(bil3.getText());
                 hsl = a1/a2/a3;
                 hasil.setText(" "+ a1 + " : "+ a2 + " : " + a3 + " = "+ hsl );
             }
             if(jml == 4) {
                 a3 = Double.parseDouble(bil3.getText());
                 a4 = Double.parseDouble(bil4.getText());
                 hsl = a1/a2/a3/ a4;
                 hasil.setText(" "+ a1 + " : "+ a2 + " : " + a3 + " : " + a4 + " = " + hsl );
             }
         }
    
    }//GEN-LAST:event_hitungActionPerformed

